# Q6] Python Program to Swap Two Variables

num1 = int(input("Enter number 1 : "))
num2 = int(input("Enter number 2 : "))

print("Before Swapping:-")
print("Number 1 : {}".format(num1))
print("Number 1 : {}".format(num2))
temp = num1
num1 = num2
num2 = temp 

print("After Swapping:-")


print("Number 1 : {}".format(num1))
print("Number 1 : {}".format(num2))